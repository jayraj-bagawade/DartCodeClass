void main() {
  int clr = 7;

  if (clr == 1) {
    print("Voilet");
  } else if (clr == 2) {
    print("Indigo");
  } else if (clr == 3) {
    print("Blue");
  } else if (clr == 4) {
    print("Green ");
  } else if (clr == 5) {
    print("Yellow");
  } else if (clr == 6) {
    print("Orange");
  } else if (clr == 7) {
    print("Red");
  } else {
    print("Enter Valid Input");
  }
}
